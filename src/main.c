/* runs a single test case */
bool pa2_run_test_case(struct pa2_test_case test_case)
{
    // extract the first matrix out of its serialized form
    uint8_t *byte_array_0 = (uint8_t *)malloc(test_case.byte_array_0_size_bytes);
    memcpy(byte_array_0, test_case.byte_array_0, test_case.byte_array_0_size_bytes);
    struct matrix matrix_0 = pa2_deserialize_matrix_in_row_major_order(test_case.byte_array_0, test_case.byte_array_0_size_bytes);
    free(byte_array_0);

    // extract the second matrix out of its serialized form
    uint8_t *byte_array_1 = (uint8_t *)malloc(test_case.byte_array_1_size_bytes);
    memcpy(byte_array_1, test_case.byte_array_1, test_case.byte_array_1_size_bytes);
    struct matrix matrix_1 = pa2_deserialize_matrix_in_row_major_order(test_case.byte_array_1, test_case.byte_array_1_size_bytes);
    free(byte_array_1);

    // perform the matrix multiply and check the results
    struct matrix computed_matrix = pa2_matrix_multiply(matrix_0, matrix_1);
    bool test_passed = pa2_check_matrices_are_equal(test_case.expected_matrix, computed_matrix);
    if(!test_passed)
    {
        pa2_print_matrix("    > A:                ", matrix_0, true);
        pa2_print_matrix("    > B:                ", matrix_1, true);
        pa2_print_matrix("    > A * B (computed): ", computed_matrix, true);
        pa2_print_matrix("    > A * B (expected): ", test_case.expected_matrix, true);
    }
    
    // clean up the allocated memory
    pa2_release_matrix(matrix_0);
    pa2_release_matrix(matrix_1);
    pa2_release_matrix(computed_matrix);
    return test_passed;
}

/*
 * Runs all test cases and reports the results
 */
int main(int argc, char **argv)
{
    // run each test case and report any failed test cases
    uint32_t passed_tests = 0;
    size_t num_test_cases = sizeof(test_cases) / sizeof(test_cases[0]);
    for(uint32_t test_case_idx = 0; test_case_idx < num_test_cases; test_case_idx++)
    {
        printf("> Running test_cases[%2u / %2zu]\n", test_case_idx, num_test_cases);

        struct pa2_test_case test_case = test_cases[test_case_idx];
        if(pa2_run_test_case(test_case))
            passed_tests++;
        else
            printf("%stest_cases[%2u] failed!%s\n", ANSI_COLOR_RED, test_case_idx, ANSI_COLOR_RESET);
    }

    puts("Testing complete!");
    if(passed_tests == num_test_cases)
        printf("%sAll %u test cases passed!%s\n", ANSI_COLOR_GREEN, passed_tests, ANSI_COLOR_RESET);
    else
        printf("%sPassed %u of %zu test cases!%s\n", ANSI_COLOR_RED, passed_tests, num_test_cases, ANSI_COLOR_RESET);
    return EXIT_SUCCESS;
}